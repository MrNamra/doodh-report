$('#logout').on('submit', function(e){
  e.preventDefault();
  $.ajax({
    url:$(this).attr('action'),
    type:"post",
    data: {_token: "{{ csrf_token() }}"},
    success:function(res){
      Toast.fire({
        icon:"success",
        title: "Logout success"
      })
      setTimeout(()=>window.location.reload(), 1000)
    }
  })
})
